class numComplejo:
    def __init__(self, numI, numJ):
        self.numI = numI
        self.numJ = numJ
    
    def suma (self, suma):
        suma
    def resta (self, resta):
        resta
    def multiplicacion (self, multiplicacion):
        multiplicacion
    def division (self, division):
        division
    def __str__(self):
        return f"{self.numI} {self.numJ}"

